# Store following word meanings in a python dictionary :
# table : “a piece of furniture”, "list of facts & figures"
# cat : "a small animal"

dictionary = {
    "table": ["a piece of furniture", "list of facts & figures"],
    "cat": ["a small animal"]
}

print("Meanings of 'table':", dictionary["table"])
